    <br/>

	<div class="container">
		<div class="row">
			<div class="col-sm">
                
                <p class="paragraphe">
                    Pour nous faciliter la tâche, nous avons commencé par déposer les fauteuils et la banquette.
                    De plus, sur ce modèle de 205, les fauteuils sont déhoussables ce qui facilite certaines tâches de nettoyage. 
                </p>
                <br/>
                <p class="paragraphe">
                    Pour le nettoyage des moquettes, nous avons commencé par désincruster les salissures avec du vinaigre et du bicarbonate. La réaction produite commence par faire un premier nettoyage.
                    Nous avons ensuite nettoyé les moquettes avec des cristaux de soude dilués dans de l'eau et de la lessive.
                </p>
                <br/>
                <p class="paragraphe">
                    Enfin, après le séchage, nous avons utilisé de l'amoniaque et une brosse sur housses des sièges et la moquette pour raviver les couleurs d'origine.
                    <span class="badge badge-accent">Attention</span> l'amoniaque peut être très dangereux, portez des gants, des lunettes, un masque et mettez-vous dans un espace aéré. 
                </p>
