	<h1>Rénovation de l'intérieur de la voiture</h1>
    <br/>
	<p class="paragraphe">
        Lorsque nous avons acheté notre Peugeot 205, un travail conséquent sur le nettoyage de l'intérieur nous attendait. 
        En effet, les sièges étaient sales, voir tachés par endroits, la couleur d'origine des moquettes avant commençait à être discutable et l'odeur n'était pas forcément la plus agréable.
        Nous nous sommes donc mis au travail afin de nous sentir bien pour les nombreux kilomètres que nous allons passer à son bord.
	</p>
