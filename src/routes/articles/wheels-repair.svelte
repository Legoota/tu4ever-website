	</p>

    <br/>
    <br/>
    <h3>Étape 6 : vernis</h3>
    <br/>

    <p class="paragraphe">
        Enfin, après séchage des deux couches de peinture, nous appliquons, de la même manière, deux couches de vernis (monocomposant, ou 1K, dans notre cas) sur les jantes.
	</p>

