<div class="content">
	<h1>Réfection des jantes 205 GTI 1.6L</h1>
    <br/>
	<p class="paragraphe">
		Après avoir acheté notre Peugeot 205, nous avons rapidement émis l'idée de l'équiper de jantes en aluminium.
        Nous sommes rapidement tombés amoureux du design des jantes de 205 GTI (version  1.6L). 
        Nous avons alors fait l'aquisition de quatre jantes dans un mauvais état esthétique.
	</p>

    <p class="paragraphe">
        Nous nous sommes donnés l'objectif de réaliser la réfection de nos jantes directement à la maison,
        en utilisant du matériel accessible aisément pour les particuliers. Le résultat attendu n'est évidemment pas
        celui que nous pourrions obtenir en nous rendant chez un professionnel, ou en utilisant des outils dédiés.
    </p>

    <p class="paragraphe">
        Voici les différentes étapes que nous avons réalisé pour rendre à nos jantes un bel aspect.
    </p>

    <br/>
    <br/>
    <h3>Étape préliminaire</h3>
    <br/>

    <p class="paragraphe">
        Lors de l'achat de nos jantes, nous avons bien vérifié que celles-ci n'étaient
        <span class="tertiary-color">pas fissurées</span>. Dans notre cas, les quatre jantes ne comportaient que
        des défauts d'ordre esthétique, donc facilement réparables.
	</p>

    <p class="paragraphe">
        Il est important de garder à l'esprit que les jantes en aluminium n'utilisent pas les mêmes boulons que celles en tôle :
        Les jantes de 205 GTI nécessitent des boulons à <span class="tertiary-color">assise plate</span>.
	</p>
    <div class="text-center">
        <figure class="figure">
            <img src="/images/wheels_repair/1.jpg" class="figure-img img-fluid rounded" style="width: 80%;" alt="Pire jante">
            <figcaption class="figure-caption text-right text-light">État de la pire jante avant travaux</figcaption>
        </figure>
    </div>

    <br/>
    <br/>
    <h3>Étape 1 : ponçage</h3>
    <br/>

    <p class="paragraphe">
        La première étape que nous avons réalisé est le ponçage de l'ensemble de chaque jante. Ce ponçage a permi de retirer
        l'ancienne peinture qui s'écaillait par endroit, et de lisser la jante sur toute sa surface.
	</p>

    <p class="paragraphe">
        Si la jante comporte un impact qui a retiré un bon morceau d'aluminium (facilement visible à l'œil nu), il peut être
        intéressant de rajouter de la matière pour égaliser la surface. Dans ce cas, un <span class="tertiary-color">mastic aluminium</span> peut vous servir.
	</p>

    <p class="paragraphe">
        Pour le ponçage, nous avons procédé en respectant les grains suivants:
	</p>
    <ol class="list">
        <li>Grain 180</li>
        <li>Grain 300</li>
        <li>Grain 450</li>
    </ol>

    <p class="paragraphe">
        Nous constatons qu'à la suite du ponçage (fastidieux), les jantes perdent leur brillant mais sont bien plus lisses au toucher.
	</p>

    <div class="text-center">
        <figure class="figure">
            <img src="/images/wheels_repair/2.jpg" class="figure-img img-fluid rounded" style="width: 80%;" alt="Pire jante après ponçage">
            <figcaption class="figure-caption text-right text-light">État de la pire jante après ponçage</figcaption>
        </figure>
    </div>

    <br/>
    <br/>
    <h3>Étape 2 : dégraissage</h3>
    <br/>

    <p class="paragraphe">
        Une fois les quatre jantes bien poncées, nous les avons correctement nettoyées pour enlever les impuretés (caoutchouc autour de la valve, poussière),
        et nous les avons dégraissées.
	</p>

    <p class="paragraphe">
        Pour notre dégraissage maison, nous avons simplement utilisé de l'eau avec du produit vaisselle ainsi qu'une éponge.
	</p>

    <br/>
    <br/>
    <h3>Étape 3 : préparation à la peinture</h3>
    <br/>

    <p class="paragraphe">
        Nous avons décidé de faire monter des pneus neufs avant de passer à l'étape de la peinture, pour éviter les potentiels accrocs
        qui peuvent survenir lors du montage. Une fois le montage et l'équilibrage des pneus réalisés, nous avons de nouveau nettoyé les
        roues de toutes les traces survenues entre temps, et nous avons masqué les pneus par du journal et du ruban adhésif de masquage.
	</p>
    <p class="paragraphe">
        <span class="badge badge-accent">Attention</span> Ne pas oublier de couvrir également les valves !
	</p>

    <div class="text-center">
        <figure class="figure">
            <img src="/images/wheels_repair/3.jpg" class="figure-img img-fluid rounded" style="width: 80%;" alt="Pire jante après ponçage">
            <figcaption class="figure-caption text-right text-light">Les quatre roues poncées et masquées</figcaption>
        </figure>
    </div>

    <p class="paragraphe">
        Important à noter: Les jantes de GTI 1.6L originales possèdent un centre dit "usiné". Le centre est donc poncé de manière circulaire jusqu'à un grain
        d'environ 400. Si vous voulez faire cette manipulation, n'oubliez pas de masquer également le centre avec du ruban adhésif. Dans notre cas,
        nous avons fait le choix de peindre le centre.
	</p>

    <br/>
    <br/>
    <h3>Étape 4 : apprêt</h3>
    <br/>

    <p class="paragraphe">
        Maintenant que nos roues sont prêtes à être peintes, nous pouvons passer à l'application de deux couches d'apprêts (ou <i>primer</i>).
        Cette étape est très importante car l'apprêt permet à la peinture de mieux tenir à la jante.
	</p>

    <p class="paragraphe">
        Nous avons utilisé des bombes d'apprêt spécial pour surfaces en métal, et après quelques recherches, de nombreuses personnes l'ont déjà utilisé pour
        leurs jantes aluminium.
	</p>

    <p class="paragraphe">
        Nous avons donc appliqué une première couche d'apprêt, que nous avons laissé sécher avant de poncer au grain 400 les petites traces restantes (projections de peinture, ...).
	</p>

    <p class="paragraphe">
        <span class="badge badge-quartiary">Conseil</span> Pour que la peinture par bombe se passe bien, veillez à suivre les préconisations du vendeur.
        Dans notre cas, nous avons attendu d'avoir une température ambiante d'au moins 12°C, et nous avons laissé sécher au moins 24h entre les différentes couches.
	</p>

    <p class="paragraphe">
        <span class="badge badge-quartiary">Conseil 2</span> Pensez à utiliser la bombe par petits sprays, dans plusieurs sens (vertical puis horizontal par exemple), afin de couvrir correctement toute la surface.
	</p>

